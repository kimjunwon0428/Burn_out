/// <summary>
/// 플레이스타일 타입 열거형
/// </summary>
public enum PlaystyleType
{
    /// <summary>
    /// 경량 - 빠른 속도, 회피 특화, 특수기 중심
    /// </summary>
    Light,

    /// <summary>
    /// 중량 - 높은 공격력/방어력, 느린 속도, 맞딜 특화
    /// </summary>
    Heavy,

    /// <summary>
    /// 가드 - 퍼펙트 가드 특화, 내구력 파괴 전문
    /// </summary>
    Guard,

    /// <summary>
    /// 스캐빈저 - 아이템/골드 획득 특화, 성장 극대화
    /// </summary>
    Scavenger
}
